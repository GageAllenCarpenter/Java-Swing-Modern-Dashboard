package io.github.gageallencarpenter.model;

/**
 * MenuList allows the application to have different menu styles. A title menu is used to
 * format the title of a menu. A menu is used to format the menu items. Empty is used to
 * format the menu items that are not used and display nothing.
 *
 * @author <a href="https://www.linkedin.com/in/gage-carpenter-07750a174/">Gage Carpenter</a>
 */
public enum MenuList {
    TITLE, MENU, EMPTY
}
